#include "ft_printf.h"

int	ft_str(char *str)
{
	size_t	i;

	if (!str)
		return (ft_str("(null)"));
	i = 0;
	while (str[i])
	{
		if (write(1, &str[i], 1) == -1)
			return (-1);
		i++;
	}
	return (i);
}
